public class OkulBilgileri {
        public static void main(String[] args) {
            int sinifMevcudu = 5; // Sınıf mevcudu belirlenir
            System.out.println("Sınıf Mevcudu: " + sinifMevcudu); // Sınıf mevcudu sayısının çıktısı alınır

            double notOrtalamasi = 92.7; // Sınıfın not ortalaması belirlenir
            System.out.println("Sınıf Not Ortalaması: " + notOrtalamasi); // Sınıfın not ortalamasının çıktısı alınır
            
            boolean sinifGectiMi = true; // Sınıfın geçilip geçilmediğine true/false (olumlu/olumsuz) olarak karar verilir
            System.out.println("Sinif Geçti Mi?: " + sinifGectiMi); // Sınıfın geçilip geçilmediğinin çıktısı alınır
        }
}    