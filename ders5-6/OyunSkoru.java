public class OyunSkoru {
    public static void main(String[] args) {
        int puan = 0;
        System.out.println("Başlangıç Puanı: " + puan);
        puan = puan + 10;
        System.out.println("Yeni Puan: " + puan);
        puan += 5;
        System.out.println("Güncellenmiş Puan: " + puan);
        puan = puan - 3;
        System.out.println("Son Puan: " + puan);
    }
}
